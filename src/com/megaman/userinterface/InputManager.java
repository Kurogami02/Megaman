package com.megaman.userinterface;

import java.awt.event.KeyEvent;  

public class InputManager {
	
	public void processKeyPressed(int keyCode) {
		
		switch(keyCode) {
			
		case KeyEvent.VK_W:
			
			break;	
			
		case KeyEvent.VK_S:
			
			break;	
			
		case KeyEvent.VK_A:
			
			break;	
			
		case KeyEvent.VK_D:
			
			break;
			
		case KeyEvent.VK_SPACE:
			
			break;	
		case KeyEvent.VK_ENTER:
			
			break;
			
		case KeyEvent.VK_F:
			System.out.print("\nPressed A \n");
			break;
		
		}
		
	}
	
public void processKeyReleased(int keyCode) {
		
		switch(keyCode) {
			
		case KeyEvent.VK_W:
			
			break;	
			
		case KeyEvent.VK_S:
			
			break;	
			
		case KeyEvent.VK_A:
			
			break;	
			
		case KeyEvent.VK_D:
			
			break;
			
		case KeyEvent.VK_SPACE:
			
			break;	
		case KeyEvent.VK_ENTER:
			
			break;
			
		case KeyEvent.VK_F:
			System.out.print("\nReleased A \n");
			break;
		
		}
		
	}
}
